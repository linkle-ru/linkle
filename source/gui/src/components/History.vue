<template>
  <div>
    <p class="subheading font-weight-thin text-xs-center text-uppercase mt-4">
      История
    </p>
    <!-- todo: добавить поиск -->
    <v-layout
      align-center
      justify-center
      column-
    >
      <v-flex
        v-if="source.length"
        xs12
        lg10
        xl8
      >
        <v-card>
          <v-list two-line>
            <template v-for="(link, index) in source">
              <v-hover
                :key="link.short_url"
              >
                <v-list-tile
                  slot-scope="{ hover }"
                  avatar
                  @click="followLink(link)"
                >
                  <v-list-tile-avatar>
                    <v-layout
                      align-center
                      justify-center
                      column
                      fill-height
                    >
                      <v-tooltip bottom>
                        <v-flex slot="activator">
                          {{ link.visits || 'N/A' }}
                        </v-flex>
                        <!--todo: реализовать-->
                        <span>В разработке</span>
                      </v-tooltip>
                      <v-icon
                        small
                        color="grey lighten-1"
                      >
                        visibility
                      </v-icon>

                    </v-layout>
                  </v-list-tile-avatar>

                  <v-list-tile-content>
                    <v-list-tile-title>
                      {{ $sanitize(link.title || "Без заголовка") }}
                    </v-list-tile-title>
                    <v-list-tile-sub-title>
                      <a
                        class="subheading"
                        target="_blank"
                        :href="link.short_url"
                      >{{ link.short_url }}</a>
                      &mdash;
                      <span class="font-weight-thin">{{ link.href }}</span>
                    </v-list-tile-sub-title>
                  </v-list-tile-content>
                  <v-list-tile-action v-show="hover">
                    <v-btn
                      icon
                      ripple
                      @click.stop="cbCopy(`${shared.origin}/${link.short_url}`)"
                    >
                      <v-icon
                        color="blue lighten-2"
                      >
                        file_copy
                      </v-icon>
                    </v-btn>
                  </v-list-tile-action>
                  <v-list-tile-action v-show="hover">
                    <v-btn
                      icon
                      ripple
                      @click.stop="deleteLink(link)"
                    >
                      <v-icon
                        color="red lighten-2"
                      >
                        remove_circle
                      </v-icon>
                    </v-btn>
                  </v-list-tile-action>
                </v-list-tile>
              </v-hover>
              <v-divider
                v-if="index + 1 < source.length"
                :key="index"
              />
            </template>
          </v-list>
        </v-card>
      </v-flex>
      <v-flex
        v-else
        md6
      >
        <v-alert
          :value="true"
          color="info"
          icon="content_cut"
          outline
        >
          У Вас пока нет сокращенных ссылок
        </v-alert>
      </v-flex>
    </v-layout>
  </div>
</template>

<script>
import {shared} from '../main'
import cbCopy from 'copy-to-clipboard'

export default {
  props: {
    source: {
      type: Array,
      required: true
    }
  },
  data: () => ({
    // todo: рефакторинг
    shared
  }),
  methods: {
    followLink(link) {
      window.open(link.href, '_blank')
    },
    deleteLink(link) {
      const index = this.source.indexOf(link)
      confirm('Вы уверены?') && this.source.splice(index, 1)
    },
    cbCopy
  }
}
</script>
